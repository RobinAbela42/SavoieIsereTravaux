    <h2>Qu'est ce qu'une donnée à caractère personnelle ?</h2>

    <p>Une donnée personnelle ou « donnée à caractère personnel » est une information se rapportant à une personne physique identifiée ou identifiable (ex : nom, prénom, numéro de sécurité sociale, adresse, numéro de téléphone, adresse mail, photo, empreinte, donnée de géolocalisation, adresse IP ou identifiant en ligne).<br><br>

Une personne est dite identifiée lorsque l'on connaît son identité. Une personne est identifiable lorsqu'elle peut être identifiée, quand bien même ses nom et prénom resteraient inconnus, à partir du croisement d'un ensemble de données (ex : une femme vivant à telle adresse, née tel jour et membre de telle association).<br><br>

En revanche, une donnée n'est plus personnelle lorsqu'elle est anonymisée, éliminant ainsi toute possibilité d'identifier la personne concernée.<br><br>

De même, une donnée n'est pas personnelle lorsqu'elle se rapporte à une personne morale (ex : une entreprise, une association).



</p>

